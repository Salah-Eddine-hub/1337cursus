#include <stdio.h>
#include <ctype.h>

int	ft_isalnum(int c)
{
	if ((c >= '0' && c <= '9')
		|| (c >= 101 && c <= 132)
		|| (c >= 141 && c <= 172))
		return (1);
	return (0);
}

